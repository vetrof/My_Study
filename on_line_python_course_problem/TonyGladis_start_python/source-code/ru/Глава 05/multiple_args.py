# Эта программа демонстрирует функцию, которая принимает
# два аргумента.

def main():
    print('Сумма чисел 12 и 45 равняется')
    show_sum(12, 45)

# Функция show_sum принимает два аргумента
# и показывает их сумму.
def show_sum(num1, num2):
    result = num1 + num2
    print(result)

# Вызвать функцию main.
main()