import random

def primary_color():
    color_list = ['red', 'green', 'orange', 'yellow', 'blue', 'magenta', 'Cyan', 'Purple']
    color = random.choice(color_list)
    return color

def autumn_color():
    color_list = ['coral', 'purple', 'ivory3', 'azure3', 'navy', 'gold', 'orange2']
    color = random.choice(color_list)
    return color